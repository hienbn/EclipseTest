package HAlgorithm;


/** THis class provides static methods for convert between convenient units.
 * 
 * @author HIENBN
 *
 */
public class HConvert {

	
	// Convert an angle from radial unit to degree unit.
	public static double radToDeg( double rad)
	{
		// return degree value.		
		return rad*Math.PI/180;		
		
	}
	
	
	
	
}
